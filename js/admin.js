async function salvarProduto(event) {
  event.preventDefault();
  
  var produto = {
    nome: document.getElementById('prodNome').value,
    preco: parseFloat(document.getElementById('prodPreco').value),
    unidade: document.getElementById('prodUnidade').value,
    categoria: document.getElementById('prodCategoria').value,
    emoji: document.getElementById('prodEmoji').value || '📦',
    estoque: parseInt(document.getElementById('prodEstoque').value) || 0
  };

  try {
    var resultado = await API.post('/admin/produtos', produto);
    
    if (resultado.erro) {
      alert('Erro: ' + resultado.erro);
      return;
    }

    alert('✅ Produto cadastrado com sucesso!');
    fecharModalProduto();
    await carregarProdutosAdmin();
    renderizarProdutosAdmin();
  } catch (erro) {
    console.error('Erro ao cadastrar produto:', erro);
    alert('Erro ao cadastrar produto');
  }
}

async function excluirProduto(id) {
  if (!confirm('Deseja realmente excluir este produto?')) return;

  try {
    await API.delete('/admin/produtos/' + id);
    alert('✅ Produto excluído com sucesso!');
    await carregarProdutosAdmin();
    renderizarProdutosAdmin();
  } catch (erro) {
    console.error('Erro ao excluir produto:', erro);
    alert('Erro ao excluir produto');
  }
}

