function verDetalhes(pedidoId) {
  var pedido = pedidos.find(function(p) { return p.id === pedidoId; });
  if (!pedido) return;

  var detalhes = 'DETALHES DO PEDIDO #' + pedido.id + '\n\n';
  detalhes += 'Cliente: ' + (pedido.cliente_nome || 'N/A') + '\n';
  detalhes += 'Email: ' + (pedido.cliente_email || 'N/A') + '\n';
  detalhes += 'Telefone: ' + (pedido.cliente_telefone || 'N/A') + '\n';
  detalhes += 'Status: ' + pedido.status + '\n';
  var totalDetalhe = parseFloat(pedido.total || 0);
  if (isNaN(totalDetalhe)) totalDetalhe = 0;
  detalhes += 'Total: R$ ' + totalDetalhe.toFixed(2).replace('.', ',') + '\n\n';
  
  if (pedido.endereco) {
    detalhes += 'ENDEREÇO DE ENTREGA:\n';
    detalhes += pedido.endereco.rua + ', ' + pedido.endereco.numero + '\n';
    detalhes += pedido.endereco.bairro + ' - ' + pedido.endereco.cidade + '/' + pedido.endereco.estado + '\n';
    detalhes += 'CEP: ' + pedido.endereco.cep + '\n';
  }

  alert(detalhes);
}

function atualizarEstatisticas() {
  var totalPedidos = pedidos.length;
  var pedidosPendentes = pedidos.filter(function(p) { return p.status === 'pendente'; }).length;
  
  var faturamento = 0;
  pedidos.forEach(function(p) {
    if (p.status !== 'cancelado') {
      faturamento += parseFloat(p.total);
    }
  });

  var hoje = new Date().toDateString();
  var pedidosHoje = pedidos.filter(function(p) {
    return new Date(p.criado_em).toDateString() === hoje;
  }).length;

  document.getElementById('totalPedidos').textContent = totalPedidos;
  document.getElementById('pedidosPendentes').textContent = pedidosPendentes;
  document.getElementById('faturamentoTotal').textContent = 'R$ ' + faturamento.toFixed(2).replace('.', ',');
  document.getElementById('pedidosHoje').textContent = pedidosHoje;
}

