async function importarCSV() {
  var arquivo = document.getElementById('csvFile').files[0];
  
  if (!arquivo) {
    alert('Selecione um arquivo CSV');
    return;
  }

  var leitor = new FileReader();
  
  leitor.onload = async function(e) {
    var conteudo = e.target.result;
    var linhas = conteudo.split('\n');
    var produtosImportar = [];

    // Pular primeira linha (cabeçalho)
    for (var i = 1; i < linhas.length; i++) {
      if (!linhas[i].trim()) continue;
      
      var colunas = linhas[i].split(',');
      
      if (colunas.length >= 6) {
        produtosImportar.push({
          nome: colunas[0].trim(),
          preco: parseFloat(colunas[1].trim()),
          unidade: colunas[2].trim(),
          categoria: colunas[3].trim(),
          emoji: colunas[4].trim() || '📦',
          estoque: parseInt(colunas[5].trim()) || 0
        });
      }
    }

    if (produtosImportar.length === 0) {
      alert('Nenhum produto válido encontrado no CSV');
      return;
    }

    try {
      var resultado = await API.post('/admin/produtos/bulk', { produtos: produtosImportar });
      
      if (resultado.erro) {
        alert('Erro: ' + resultado.erro);
        return;
      }

      alert('✅ ' + produtosImportar.length + ' produtos importados com sucesso!');
      document.getElementById('csvFile').value = '';
      await carregarProdutosAdmin();
      trocarView('produtos');
    } catch (erro) {
      console.error('Erro ao importar:', erro);
      alert('Erro ao importar produtos');
    }
  };

  leitor.readAsText(arquivo);
}
