async function carregarPedidos() {
  try {
    var resultado = await API.get('/admin/pedidos');
    if (resultado.pedidos) {
      pedidos = resultado.pedidos;
    }
  } catch (erro) {
    console.error('Erro ao carregar pedidos:', erro);
  }
}

async function carregarProdutosAdmin() {
  try {
    var resultado = await API.get('/produtos');
    if (resultado.produtos) {
      produtos = resultado.produtos;
    }
  } catch (erro) {
    console.error('Erro ao carregar produtos:', erro);
  }
}

function configurarViews() {
  document.querySelectorAll('[data-view]').forEach(function(tab) {
    tab.addEventListener('click', function() {
      var view = tab.getAttribute('data-view');
      trocarView(view);
    });
  });
}

function trocarView(view) {
  viewAtual = view;
  
  // Atualizar tabs
  document.querySelectorAll('[data-view]').forEach(function(tab) {
    tab.classList.remove('active');
    if (tab.getAttribute('data-view') === view) {
      tab.classList.add('active');
    }
  });

  // Esconder todas as views
  document.querySelectorAll('.admin-view').forEach(function(v) {
    v.style.display = 'none';
  });

  // Mostrar view selecionada
  if (view === 'pedidos') {
    document.getElementById('viewPedidos').style.display = 'block';
    renderizarPedidos();
  } else if (view === 'produtos') {
    document.getElementById('viewProdutos').style.display = 'block';
    renderizarProdutosAdmin();
  } else if (view === 'importar') {
    document.getElementById('viewImportar').style.display = 'block';
  }
}

function configurarFiltros() {
  document.querySelectorAll('[data-status]').forEach(function(tab) {
    tab.addEventListener('click', function() {
      document.querySelectorAll('[data-status]').forEach(function(t) {
        t.classList.remove('active');
      });
      tab.classList.add('active');
      statusFiltro = tab.getAttribute('data-status');
      renderizarPedidos();
    });
  });
}

function renderizarPedidos() {
  var lista = document.getElementById('pedidosList');
  
  var pedidosFiltrados = pedidos.filter(function(pedido) {
    return statusFiltro === 'todos' || pedido.status === statusFiltro;
  });

  console.log('Pedidos carregados:', pedidos.length);
  console.log('Pedidos filtrados:', pedidosFiltrados.length);

  if (pedidosFiltrados.length === 0) {
    lista.innerHTML = '<p style="text-align: center; color: #64748b; padding: 2rem;">Nenhum pedido encontrado</p>';
    return;
  }

  lista.innerHTML = '';
  
  pedidosFiltrados.forEach(function(pedido) {
    var card = document.createElement('div');
    card.className = 'pedido-card';
    
    var dataPedido = new Date(pedido.criado_em);
    var dataFormatada = dataPedido.toLocaleDateString('pt-BR') + ' às ' + dataPedido.toLocaleTimeString('pt-BR');
    
    var statusClass = 'status-' + pedido.status;
    var statusTexto = {
      'pendente': 'Pendente',
      'preparando': 'Preparando',
      'enviado': 'Enviado',
      'entregue': 'Entregue',
      'cancelado': 'Cancelado'
    };

    var itensHTML = '';
    if (pedido.itens && pedido.itens.length > 0) {
      pedido.itens.forEach(function(item) {
        var subtotal = parseFloat(item.subtotal !== undefined && item.subtotal !== null ? item.subtotal : item.preco || 0);
        if (isNaN(subtotal)) subtotal = 0;
        itensHTML += '<div class="item-linha">' +
          '<span>' + item.quantidade + 'x ' + item.nome_produto + '</span>' +
          '<span>R$ ' + subtotal.toFixed(2).replace('.', ',') + '</span>' +
        '</div>';
      });
    }

    var totalPedido = parseFloat(pedido.total);
    if (isNaN(totalPedido)) totalPedido = 0;

    card.innerHTML = 
      '<div class="pedido-header">' +
        '<div>' +
          '<h3>Pedido #' + pedido.id + '</h3>' +
          '<p class="cliente-info">👤 Cliente: ' + (pedido.cliente_nome || 'N/A') + '</p>' +
          '<p class="cliente-info">📅 ' + dataFormatada + '</p>' +
          '<p class="cliente-info">💳 Pagamento: ' + (pedido.forma_pagamento === 'pix' ? 'PIX' : 'Na Entrega') + '</p>' +
        '</div>' +
        '<span class="pedido-status ' + statusClass + '">' + statusTexto[pedido.status] + '</span>' +
      '</div>' +
      '<div class="pedido-itens">' +
        '<strong>Itens do Pedido:</strong>' +
        itensHTML +
      '</div>' +
      '<div style="display: flex; justify-content: space-between; align-items: center; margin-top: 1rem; padding-top: 1rem; border-top: 1px solid #e2e8f0;">' +
        '<strong style="font-size: 1.2rem; color: var(--primary);">Total: R$ ' + (isNaN(totalPedido) ? '0,00' : totalPedido.toFixed(2).replace('.', ',')) + '</strong>' +
        '<div class="pedido-acoes">' +
          (pedido.status === 'pendente' ? 
            '<button class="btn btn-small" onclick="mudarStatus(' + pedido.id + ', \'preparando\')">📦 Preparar</button>' : '') +
          (pedido.status === 'preparando' ? 
            '<button class="btn btn-small" onclick="mudarStatus(' + pedido.id + ', \'enviado\')">🚚 Enviar</button>' : '') +
          (pedido.status === 'enviado' ? 
            '<button class="btn btn-small" onclick="mudarStatus(' + pedido.id + ', \'entregue\')">✅ Entregar</button>' : '') +
          '<button class="btn btn-small" onclick="imprimirNotaFiscal(' + pedido.id + ')">🧾 NF teste</button>' +
          '<button class="btn btn-secondary btn-small" onclick="imprimirNotaEntregaAdmin(' + pedido.id + ')">🖨️ Imprimir Nota</button>' +
        '</div>' +
      '</div>';
    
    lista.appendChild(card);
  });
}

async function mudarStatus(pedidoId, novoStatus) {
  try {
    var resultado = await API.put('/admin/pedidos/' + pedidoId + '/status', { status: novoStatus });
    
    if (resultado.erro) {
      alert('Erro: ' + resultado.erro);
      return;
    }

    alert('✅ Status atualizado com sucesso!');
    await carregarPedidos();
    renderizarPedidos();
    atualizarEstatisticas();
  } catch (erro) {
    console.error('Erro ao mudar status:', erro);
    alert('Erro ao atualizar status');
  }
}

function imprimirNotaFiscal(pedidoId) {
  var pedido = pedidos.find(function(p) { return p.id === pedidoId; });
  if (!pedido) return;

  var dataPedido = new Date(pedido.criado_em);
  var dataFormatada = dataPedido.toLocaleDateString('pt-BR') + ' ' + dataPedido.toLocaleTimeString('pt-BR');
  var totalNumero = Number(pedido.total) || 0;
  var logoDataUri = obterLogoDataUri();
  var logoSrc = (window.location && window.location.origin ? window.location.origin : '') + '/img/logo-cupom.png';
  var finalLogoSrc = logoSrc || logoDataUri;

  function fmt(valor) {
    var n = Number(valor);
    if (!isFinite(n)) n = 0;
    return n.toFixed(2).replace('.', ',');
  }

  function pad(val, len) {
    return (val + ' '.repeat(len)).substring(0, len);
  }

  function formatLine(nome, qtd, unit, sub) {
    var nomeLimpo = (nome || '').substring(0, 24);
    var q = pad(String(qtd || 1), 4);
    var u = pad(fmt(unit), 8);
    var s = pad(fmt(sub), 8);
    return nomeLimpo + '\n ' + q + ' x ' + u + '  ' + s;
  }

  var itensTexto = '';
  if (pedido.itens) {
    pedido.itens.forEach(function(item, idx) {
      var precoUnit = Number(item.preco || item.valor || 0);
      var subtotalCalc = item.subtotal !== undefined && item.subtotal !== null
        ? Number(item.subtotal)
        : precoUnit * (item.quantidade || 1);
      if (!isFinite(precoUnit)) precoUnit = 0;
      if (!isFinite(subtotalCalc)) subtotalCalc = 0;
      itensTexto += pad(String(idx + 1).padStart(3, '0'), 4) + ' ' + formatLine(item.nome_produto || item.nome || 'Produto', item.quantidade || 1, precoUnit, subtotalCalc) + '\n';
    });
  }

  var header = [
    '        COMERCIO BOM FILHO',
    'END: 7 DE SETEMBRO',
    'FONE: (91)99965-2790 / 98106-6227 / 3721-9780',
    'DATA: ' + dataFormatada + '   PED: ' + pedido.id
  ].join('\n');

  var pagamento = 'FORMA PAGAMENTO: ' + (pedido.forma_pagamento ? pedido.forma_pagamento.toUpperCase() : '---');

  var corpo =
    header + '\n' +
    '--------------------------------' + '\n' +
    'ITENS:' + '\n' +
    itensTexto +
    '--------------------------------' + '\n' +
    'QTDE ITENS: ' + (pedido.itens ? pedido.itens.length.toString().padStart(3, '0') : '000') + '\n' +
    'TOTAL R$  ' + fmt(totalNumero) + '\n' +
    pagamento + '\n' +
    '--------------------------------' + '\n' +
    'Obrigado pela preferência!';

  var notaHTML = '<!DOCTYPE html>' +
    '<html><head><meta charset="utf-8"><title>Nota - Pedido #' + pedido.id + '</title>' +
    '<style>' +
      'body{margin:0;display:flex;justify-content:center;background:#fff;}' +
      '.cupom{width:360px;padding:14px;}' +
      '.logo-block{text-align:center;margin:4px 0 8px;}' +
      '.logo-img{max-width:200px;width:100%;height:auto;object-fit:contain;}' +
      '.texto{white-space:pre-wrap;margin:0;font-family:"Consolas","Courier New",monospace;font-size:13px;line-height:1.35;}' +
    '</style></head><body>' +
    '<div class="cupom">' +
      '<div class="logo-block">' +
        '<img class="logo-img" src="' + finalLogoSrc + '" alt="Comércio Bom Filho" onerror="this.onerror=null; this.src=\'' + logoDataUri + '\';">' +
      '</div>' +
      '<pre class="texto">' + corpo + '</pre>' +
    '</div>' +
    '<script>window.print();</script>' +
    '</body></html>';

  var janelaImpressao = window.open('', '', 'width=800,height=600');
  janelaImpressao.document.write(notaHTML);
  janelaImpressao.document.close();
  janelaImpressao.print();
}

